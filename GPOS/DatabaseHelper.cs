using System;
using System.Collections.Generic;
using System.Data.SqlClient;
using System.Linq;
using System.Text;
using System.Threading.Tasks;

namespace GPOS
{
    public static class DatabaseHelper
    {
        private static readonly string connectionString =
        "Data Source=ServerName;" +   // Replace with your server name
        "Initial Catalog=DatabaseName;" + // Replace with your database name
        "User ID=UserName;" +          // Replace with your database username
        "Password=UserPassword;";      // Replace with your database password

        // Method to get a new SqlConnection
        public static SqlConnection GetConnection()
        {
            // Return a new SqlConnection with the connection string
            return new SqlConnection(connectionString);
        }

    }
}
